package com.kb.employee.entity;

public class Employee {

	private String emp_firstName;
	private String emp_lastName;
	private String emp_userid;
	private String emp_password;
	private String emp_reenterpassword;
	private String emp_accesstype;
	public String getEmp_firstName() {
		return emp_firstName;
	}
	public void setEmp_firstName(String emp_firstName) {
		this.emp_firstName = emp_firstName;
	}
	public String getEmp_lastName() {
		return emp_lastName;
	}
	public void setEmp_lastName(String emp_lastName) {
		this.emp_lastName = emp_lastName;
	}
	public String getEmp_userid() {
		return emp_userid;
	}
	public void setEmp_userid(String emp_userid) {
		this.emp_userid = emp_userid;
	}
	public String getEmp_password() {
		return emp_password;
	}
	public void setEmp_password(String emp_password) {
		this.emp_password = emp_password;
	}
	public String getEmp_reenterpassword() {
		return emp_reenterpassword;
	}
	public void setEmp_reenterpassword(String emp_reenterpassword) {
		this.emp_reenterpassword = emp_reenterpassword;
	}
	public String getEmp_accesstype() {
		return emp_accesstype;
	}
	public void setEmp_accesstype(String emp_accesstype) {
		this.emp_accesstype = emp_accesstype;
	}

	
}
